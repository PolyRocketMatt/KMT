package com.github.polyrocketmatt.kmt.common

class Maths {

    companion object {
        /**
         * Adds two integers together.
         *
         * @param a The first integer.
         * @param b The second integer.
         * @return The sum of the two integers.
         */
        fun add(a: Int, b: Int): Int {
            return a + b
        }
    }

}